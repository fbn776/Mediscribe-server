const conversationsSchema = new mongoose.Schema({
    session: { type: mongoose.Schema.Types.ObjectId, ref: "sessions", required: true },
    message_id: { type: String, required: true }, // id of message, generated by stt service
    speaker: { type: String, required: true },
    text: { type: String, required: true },
    type: { type: String, required: true }
}, {
    timestamps: true
});




const Conversations = mongoose.model("conversations", conversationsSchema);

export default Conversations;