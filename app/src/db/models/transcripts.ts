const transcriptsSchema = new mongoose.Schema({
    session: { type: mongoose.Schema.Types.ObjectId, ref: "sessions", required: true, index: true},
    message_id: { type: String, required: true, index: true }, // id of message, generated by stt service
    speaker: { type: String, required: true },

    raw_text: { type: String, required: true },

    processed_text: String,

    corrected_text: String,
    concise_text: String,
    highlighted_text: String,
}, {
    timestamps: true
});

const Transcripts = mongoose.model("transcripts", transcriptsSchema);

export default Transcripts;